package us.telran.pawnshop.entity.enums;

/**
 * Defines the types of items that can be encountered or managed within the system.
 * This enumeration is used to categorize various kinds of jewelry and related items,
 * facilitating their identification, cataloging, and processing in inventory or sales operations.
 * <p>
 * The item types include:
 * <ul>
 *     <li>{@code CHAIN} - A flexible series of linked metal rings or segments, often worn as jewelry.</li>
 *     <li>{@code NECKLACE} - Jewelry worn around the neck, typically a chain or band with or without a pendant.</li>
 *     <li>{@code BRACELET} - A decorative band or chain worn on the wrist.</li>
 *     <li>{@code GIMBAL} - Possibly a specialized item or tool, often related to mechanical or jewelry designs (Note: "Gimbal" is usually a mechanical device, its inclusion here might be specific or an error).</li>
 *     <li>{@code RING} - A small circular band, typically of metal, worn as ornamental jewelry around the finger.</li>
 *     <li>{@code SIGNET_RING} - A ring engraved with a signature seal, traditionally used to sign or seal documents.</li>
 *     <li>{@code EARRINGS} - Jewelry worn on the earlobes or ear, encompassing a wide range of styles from studs to hoops.</li>
 *     <li>{@code WATCH} - A timepiece worn typically on the wrist.</li>
 *     <li>{@code CUFF_LINKS} - Decorative fasteners used to secure the cuffs of a shirt.</li>
 *     <li>{@code BROOCH} - A decorative jewelry item designed to be attached to garments, often to hold them closed.</li>
 *     <li>{@code TOOTH} - Could refer to an ornamental piece made from or incorporating an animal tooth.</li>
 *     <li>{@code COIN} - A piece of currency or collectible item, sometimes set into jewelry.</li>
 *     <li>{@code SCRAP} - Pieces of precious metal that are left over or discarded.</li>
 * </ul>
 *
 * @author bh-alexey
 *
 */

public enum ItemType {
    CHAIN,
    NECKLACE,
    BRACELET,
    GIMBAL,
    RING,
    SIGNET_RING,
    EARRINGS,
    WATCH,
    CUFF_LINKS,
    BROOCH,
    TOOTH,
    COIN,
    SCRAP
}
